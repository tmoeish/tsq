package cmd

import (
	"bytes"
	_ "embed"
	"fmt"
	"os"
	"path/filepath"
	"strings"
	"text/template"

	"github.com/juju/errors"
	"github.com/spf13/cobra"
	"github.com/tmoeish/tsq"
	"github.com/tmoeish/tsq/internal/parser"
	"mvdan.cc/gofumpt/format"
)

var (
	//go:embed tsq.go.tmpl
	defaultTableTpl string

	//go:embed tsq_dto.go.tmpl
	defaultDTOTpl string

	tplFlag    string
	dtoTplFlag string
	v          bool
)

const generatedFileHeaderPrefix = "// Code generated by tsq-"

const (
	primitiveByte   = "byte"
	primitiveUInt64 = "uint64"
)

func init() {
	GenCmd.Flags().StringVarP(&tplFlag, "tpl", "t", "", "tsq template file")
	GenCmd.Flags().StringVarP(&dtoTplFlag, "dtotpl", "", "", "tsq dto template file")
	GenCmd.Flags().BoolVarP(&v, "verbose", "v", false, "verbose output")
}

var GenCmd = &cobra.Command{
	Use:   "gen [package]",
	Short: "`gen` generates tsq.go file for each table in package",
	RunE: func(cmd *cobra.Command, args []string) error {
		if len(args) < 1 {
			_ = cmd.Usage()
			return errors.New("package path is required")
		}

		tableTpl, err := resolveTemplateText(tplFlag, defaultTableTpl, "template")
		if err != nil {
			return errors.Trace(err)
		}

		dtoTpl, err := resolveTemplateText(dtoTplFlag, defaultDTOTpl, "DTO template")
		if err != nil {
			return errors.Trace(err)
		}

		pPath := args[0]
		list, dir, err := parser.Parse(pPath)
		if err != nil {
			return errors.Trace(err)
		}

		for i := range list {
			list[i].SetTSQVersion(tsq.Version)
		}

		if err := validateGeneratedFilenameCollisions(list); err != nil {
			return errors.Trace(err)
		}
		if err := validateIndexNameCollisions(list); err != nil {
			return errors.Trace(err)
		}
		if err := validateGeneratedSymbolCollisions(list); err != nil {
			return errors.Trace(err)
		}

		structsByName := make(map[string]*tsq.StructInfo, len(list))
		for _, s := range list {
			structsByName[s.TypeInfo.TypeName] = s
		}

		tpl, err := template.New("tsq.go.tmpl").Funcs(TemplateFuncs()).Parse(tableTpl)
		if err != nil {
			return errors.Annotate(err, "failed to parse table template")
		}

		dtoTplParsed, err := template.New("tsq_dto.go.tmpl").Funcs(TemplateFuncs()).Parse(dtoTpl)
		if err != nil {
			return errors.Annotate(err, "failed to parse DTO template")
		}

		for _, s := range list {
			if s.TableInfo == nil || len(s.Fields) == 0 {
				continue
			}
			if err := validateStructForGeneration(s, structsByName); err != nil {
				return errors.Annotatef(err, "failed to validate %s", s.TypeInfo.TypeName)
			}
			if s.IsDTO {
				normalizeDTOColumns(s)
				if err := genDTO(s, dtoTplParsed, dir); err != nil {
					return errors.Trace(err)
				}
			} else {
				if err := gen(s, tpl, dir); err != nil {
					return errors.Trace(err)
				}
			}
		}

		return nil
	},
}

func resolveTemplateText(overridePath string, fallback string, label string) (string, error) {
	if len(overridePath) == 0 {
		return fallback, nil
	}

	tplBytes, err := os.ReadFile(overridePath)
	if err != nil {
		return "", errors.Annotatef(err, "failed to read %s file: %s", label, overridePath)
	}

	return string(tplBytes), nil
}

func gen(data *tsq.StructInfo, t *template.Template, dir string) error {
	filename := generatedFilename(data)
	filename = filepath.Join(dir, filename)

	if v {
		_, _ = fmt.Fprintf(os.Stderr, "gen %s\n", filename)
	}

	buf := new(bytes.Buffer)

	if err := t.Execute(buf, data); err != nil {
		bs := tsq.PrettyJSON(data)
		return errors.Annotatef(err, "template rendering failed: %s, data: %s", filename, bs)
	}

	src, err := format.Source(buf.Bytes(), format.Options{})
	if err != nil {
		return errors.Annotatef(err, "Go code formatting failed: %s", filename)
	}

	err = writeGeneratedFile(filename, src)
	if err != nil {
		return errors.Annotatef(err, "failed to write file: %s", filename)
	}

	return nil
}

func genDTO(data *tsq.StructInfo, t *template.Template, dir string) error {
	filename := generatedFilename(data)
	filename = filepath.Join(dir, filename)

	if v {
		_, _ = fmt.Fprintf(os.Stderr, "gen %s\n", filename)
	}

	buf := new(bytes.Buffer)

	if err := t.Execute(buf, data); err != nil {
		bs := tsq.PrettyJSON(data)
		return errors.Annotatef(err, "DTO template rendering failed: %s, data: %s", filename, bs)
	}

	src, err := format.Source(buf.Bytes(), format.Options{})
	if err != nil {
		return errors.Annotatef(err, "Go code formatting failed: %s", filename)
	}

	err = writeGeneratedFile(filename, src)
	if err != nil {
		return errors.Annotatef(err, "failed to write file: %s", filename)
	}

	return nil
}

func validateStructForGeneration(
	data *tsq.StructInfo,
	structsByName map[string]*tsq.StructInfo,
) error {
	if data == nil || data.TableInfo == nil {
		return nil
	}

	if data.IsDTO {
		return errors.Trace(validateDTOFields(data, structsByName))
	}

	if err := validatePrimaryKeyField(data); err != nil {
		return errors.Trace(err)
	}

	if err := validateVersionField(data); err != nil {
		return errors.Trace(err)
	}

	if err := validateFieldDatabaseCompatibility(data); err != nil {
		return errors.Trace(err)
	}

	return errors.Trace(ValidateManagedFields(data))
}

func validateDTOFields(
	dto *tsq.StructInfo,
	structsByName map[string]*tsq.StructInfo,
) error {
	normalizedRefs := make(map[string]string, len(dto.Fields))

	for _, field := range dto.Fields {
		parts := strings.Split(field.Column, ".")
		if len(parts) != 2 || parts[0] == "" || parts[1] == "" {
			return errors.Errorf(
				"DTO field %s must reference a generated column as Struct.Field, got %q",
				field.Name,
				field.Column,
			)
		}

		normalizedRef := strings.ReplaceAll(field.Column, ".", "_")
		if existing, ok := normalizedRefs[normalizedRef]; ok && existing != field.Column {
			return errors.Errorf(
				"DTO field %s reference %s collides with %s after normalization",
				field.Name,
				field.Column,
				existing,
			)
		}

		normalizedRefs[normalizedRef] = field.Column

		targetStruct, ok := structsByName[parts[0]]
		if !ok || targetStruct.TableInfo == nil {
			return errors.Errorf(
				"DTO field %s references unknown struct %s",
				field.Name,
				parts[0],
			)
		}

		sourceField, ok := targetStruct.FieldMap[parts[1]]
		if !ok {
			return errors.Errorf(
				"DTO field %s references unknown field %s.%s",
				field.Name,
				parts[0],
				parts[1],
			)
		}

		if !isScanCompatible(field, sourceField) {
			return errors.Errorf(
				"DTO field %s type %s is incompatible with %s.%s type %s",
				field.Name,
				field.String(),
				parts[0],
				parts[1],
				sourceField.String(),
			)
		}
	}

	return nil
}

func isScanCompatible(dst tsq.FieldInfo, src tsq.FieldInfo) bool {
	return dst.Type == src.Type &&
		dst.IsPointer == src.IsPointer &&
		dst.IsArray == src.IsArray
}

func normalizeDTOColumns(data *tsq.StructInfo) {
	for i, field := range data.Fields {
		field.Column = strings.ReplaceAll(field.Column, ".", "_")
		data.Fields[i] = field

		mapped := data.FieldMap[field.Name]
		mapped.Column = field.Column
		data.FieldMap[field.Name] = mapped
	}
}

func validateGeneratedFilenameCollisions(list []*tsq.StructInfo) error {
	seen := make(map[string]string, len(list))

	for _, data := range list {
		if data == nil || data.TableInfo == nil || len(data.Fields) == 0 {
			continue
		}

		filename := generatedFilename(data)
		if existing, ok := seen[filename]; ok && existing != data.TypeInfo.TypeName {
			return errors.Errorf(
				"generated filename %s collides between %s and %s",
				filename,
				existing,
				data.TypeInfo.TypeName,
			)
		}

		seen[filename] = data.TypeInfo.TypeName
	}

	return nil
}

func validateIndexNameCollisions(list []*tsq.StructInfo) error {
	type definition struct {
		table  string
		fields string
		unique bool
	}

	seen := make(map[string]definition)

	for _, data := range list {
		if data == nil || data.TableInfo == nil || data.IsDTO {
			continue
		}

		for _, group := range []struct {
			unique bool
			items  []tsq.IndexInfo
		}{
			{unique: true, items: data.UxList},
			{unique: false, items: data.IdxList},
		} {
			for _, idx := range group.items {
				current := definition{
					table:  data.Table,
					fields: strings.Join(idx.Fields, ","),
					unique: group.unique,
				}

				existing, ok := seen[idx.Name]
				if !ok {
					seen[idx.Name] = current
					continue
				}

				if existing == current {
					return errors.Errorf(
						"duplicate index name %s on table %s",
						idx.Name,
						data.Table,
					)
				}

				return errors.Errorf(
					"index name %s collides between %s(%s) and %s(%s)",
					idx.Name,
					existing.table,
					existing.fields,
					current.table,
					current.fields,
				)
			}
		}
	}

	return nil
}

func validateGeneratedSymbolCollisions(list []*tsq.StructInfo) error {
	seen := make(map[string]string)

	register := func(symbol string, owner string) error {
		if symbol == "" {
			return nil
		}

		if existing, ok := seen[symbol]; ok && existing != owner {
			return errors.Errorf("generated symbol %s collides between %s and %s", symbol, existing, owner)
		}

		seen[symbol] = owner

		return nil
	}

	for _, data := range list {
		if data == nil || data.TableInfo == nil {
			continue
		}

		typeName := data.TypeInfo.TypeName
		baseSymbols := []string{
			"Table" + typeName,
			"Table" + typeName + "Cols",
			"get" + typeName + "By" + data.ID + "Query",
			"Get" + typeName + "By" + data.ID,
			"Get" + typeName + "By" + data.ID + "OrErr",
			"List" + typeName + "By" + data.ID + "In",
			"List" + typeName + "By" + data.ID + "InOrErr",
			"List" + typeName,
			"Page" + typeName,
			"Count" + typeName,
			"List" + typeName + "ByQuery",
			"Page" + typeName + "ByQuery",
		}

		if data.IsDTO {
			baseSymbols = append(baseSymbols, "Dto"+typeName)
		}

		for _, symbol := range baseSymbols {
			if err := register(symbol, typeName); err != nil {
				return err
			}
		}

		for _, field := range data.Fields {
			if err := register(typeName+"_"+field.Name, typeName); err != nil {
				return err
			}
		}

		for _, idx := range data.QueryList {
			queryName := joinAnd(idx.Fields)
			for _, symbol := range []string{
				"Count" + typeName + "By" + queryName,
				"List" + typeName + "By" + queryName,
				"Page" + typeName + "By" + queryName,
			} {
				if err := register(symbol, typeName); err != nil {
					return err
				}
			}
		}
	}

	return nil
}

func generatedFilename(data *tsq.StructInfo) string {
	base := strings.ToLower(data.TypeInfo.TypeName)
	if data.IsDTO {
		return fmt.Sprintf("%s_dto_tsq.go", base)
	}

	return fmt.Sprintf("%s_tsq.go", base)
}

func validatePrimaryKeyField(data *tsq.StructInfo) error {
	if data == nil || data.TableInfo == nil || data.ID == "" {
		return nil
	}

	field, ok := data.FieldMap[data.ID]
	if !ok {
		return errors.Errorf("id field %s not found in %s", data.ID, data.TypeInfo.TypeName)
	}

	if field.IsPointer || field.IsArray {
		return errors.Errorf(
			"id field %s in %s cannot be a pointer or slice/array type",
			data.ID,
			data.TypeInfo.TypeName,
		)
	}

	return nil
}

func validateVersionField(data *tsq.StructInfo) error {
	if data == nil || data.TableInfo == nil || data.V == "" {
		return nil
	}

	field, ok := data.FieldMap[data.V]
	if !ok {
		return errors.Errorf("version field %s not found in %s", data.V, data.TypeInfo.TypeName)
	}

	if !isIntegerFieldType(field) || field.IsPointer || field.IsArray {
		return errors.Errorf("version field %s in %s must be a non-pointer integer type", data.V, data.TypeInfo.TypeName)
	}

	return nil
}

func validateFieldDatabaseCompatibility(data *tsq.StructInfo) error {
	if data == nil || data.TableInfo == nil {
		return nil
	}

	keywordFields := make(map[string]struct{}, len(data.KwList))
	for _, field := range data.KwList {
		keywordFields[field] = struct{}{}
	}

	for _, field := range data.Fields {
		if err := validateFieldDatabaseType(field, keywordFields); err != nil {
			return errors.Annotatef(err, "field %s in %s", field.Name, data.TypeInfo.TypeName)
		}
	}

	return nil
}

func validateFieldDatabaseType(field tsq.FieldInfo, keywordFields map[string]struct{}) error {
	if field.Type.Package.Path == "" {
		switch field.Type.TypeName {
		case "complex64", "complex128", "uintptr":
			return errors.Errorf("type %s is not supported for generated columns", field.Type.TypeName)
		}
	}

	if field.IsArray {
		if field.Type.Package.Path != "" || field.Type.TypeName != primitiveByte {
			return errors.New("slice/array columns must use []byte")
		}
	}

	if _, ok := keywordFields[field.Name]; ok {
		if field.IsPointer {
			return errors.New("keyword fields cannot be pointers")
		}

		if field.IsArray {
			if field.Type.Package.Path == "" && field.Type.TypeName == primitiveByte {
				return nil
			}

			return errors.New("keyword fields must use textual types")
		}

		if field.Type.Package.Path != "" || (field.Type.TypeName != "string" && field.Type.TypeName != "rune" && field.Type.TypeName != "byte") {
			return errors.New("keyword fields must use textual types")
		}
	}

	return nil
}

func isIntegerFieldType(field tsq.FieldInfo) bool {
	if field.Type.Package.Path != "" {
		return false
	}

	switch field.Type.TypeName {
	case "int", "int8", "int16", "int32", "int64", "uint", "uint8", "uint16", "uint32", primitiveUInt64:
		return true
	default:
		return false
	}
}

func writeGeneratedFile(filename string, src []byte) error {
	if err := ensureWritableGeneratedFile(filename); err != nil {
		return err
	}

	perm := os.FileMode(0o644)
	if info, err := os.Stat(filename); err == nil {
		perm = info.Mode().Perm()
	} else if !os.IsNotExist(err) {
		return err
	}

	dir := filepath.Dir(filename)
	pattern := "." + filepath.Base(filename) + ".tmp-*"

	tmpFile, err := os.CreateTemp(dir, pattern)
	if err != nil {
		return err
	}

	tmpName := tmpFile.Name()

	cleanup := func() {
		_ = os.Remove(tmpName)
	}
	defer cleanup()

	if err := tmpFile.Chmod(perm); err != nil {
		_ = tmpFile.Close()
		return err
	}

	if _, err := tmpFile.Write(src); err != nil {
		_ = tmpFile.Close()
		return err
	}

	if err := tmpFile.Close(); err != nil {
		return err
	}

	if err := os.Rename(tmpName, filename); err != nil {
		return err
	}

	return nil
}

func ensureWritableGeneratedFile(filename string) error {
	existing, err := os.ReadFile(filename)
	if os.IsNotExist(err) {
		return nil
	}

	if err != nil {
		return err
	}

	if len(existing) == 0 || bytes.HasPrefix(existing, []byte(generatedFileHeaderPrefix)) {
		return nil
	}

	return errors.Errorf("refusing to overwrite non-generated file: %s", filename)
}
