const ddlGeneratedFileHeaderPrefix = "-- Code generated by tsq-"

const (
	ddlDialectSQLite   = "sqlite"
	ddlDialectMySQL    = "mysql"
	ddlDialectPostgres = "postgres"
)

type ddlFileModel struct {
	Filename string
	Source   []byte
}

type ddlArtifacts struct {
	models       []ddlFileModel
	firstRun     bool
	hasChange    bool
	recordTables []ddlStateRecordTable
}

type ddlDialectSpec struct {
	name    string
	dialect tsq.Dialect
}

type ddlTypeResolver struct {
	packages map[string]*packages.Package
}

type ddlColumnKind string

const (
	ddlColumnBool   ddlColumnKind = "bool"
	ddlColumnBytes  ddlColumnKind = "bytes"
	ddlColumnFloat  ddlColumnKind = "float"
	ddlColumnInt    ddlColumnKind = "int"
	ddlColumnString ddlColumnKind = "string"
	ddlColumnTime   ddlColumnKind = "time"
)

type ddlColumnDescriptor struct {
	kind     ddlColumnKind
	bits     int
	unsigned bool
	nullable bool
	size     int
}

var ddlDialects = []ddlDialectSpec{
	{name: ddlDialectSQLite, dialect: tsq.SqliteDialect{}},
	{name: ddlDialectMySQL, dialect: tsq.MySQLDialect{}},
	{name: ddlDialectPostgres, dialect: tsq.PostgresDialect{}},
}

func buildDDLArtifacts(packagePath string, list []*tsq.StructInfo, outDir string) (ddlArtifacts, error) {
	if err := validateIndexNameCollisions(list); err != nil {
		return ddlArtifacts{}, errors.Trace(err)
	}

	tables := make([]*tsq.StructInfo, 0, len(list))
	for _, item := range list {
		if item == nil || item.TableInfo == nil || item.IsResult || len(item.Fields) == 0 {
			continue
		}

		tables = append(tables, item)
	}

	sort.Slice(tables, func(i, j int) bool {
		if tables[i].Table == tables[j].Table {
			return tables[i].TypeInfo.TypeName < tables[j].TypeInfo.TypeName
		}

		return tables[i].Table < tables[j].Table
	})

	resolver, err := newDDLTypeResolver(packagePath, outDir)
	if err != nil {
		return ddlArtifacts{}, errors.Trace(err)
	}

	version := stableVersion(tsq.Version)

	currentSnapshot, err := buildCurrentDDLSnapshot(tables, resolver)
	if err != nil {
		return ddlArtifacts{}, errors.Trace(err)
	}

	previousState, err := loadDDLStateFile(outDir)
	if err != nil {
		return ddlArtifacts{}, errors.Trace(err)
	}

	var previousSnapshot *ddlSnapshot
	if previousState != nil {
		previousSnapshot = &previousState.Snapshot
	}

	changes := diffDDLSnapshots(previousSnapshot, currentSnapshot)
	recordTables := buildDDLRecordTables(changes)
	hasChange := len(recordTables) > 0
	models := make([]ddlFileModel, 0, len(ddlDialects)*2+1)
	dialectHistory := make(map[string]ddlStateDialectDiff, len(ddlDialects))
	firstRun := previousState == nil

	for _, dialect := range ddlDialects {
		models = append(models, ddlFileModel{
			Filename: filepath.Join(outDir, dialect.name+".sql"),
			Source:   renderDDLSnapshotAggregateFile(version, currentSnapshot, dialect),
		})

		incrementalModels, diffRecord, err := renderDDLIncrementalArtifacts(version, dialect, changes)
		if err != nil {
			return ddlArtifacts{}, errors.Trace(err)
		}

		if !firstRun && hasChange {
			for _, model := range incrementalModels {
				model.Filename = filepath.Join(outDir, model.Filename)
				models = append(models, model)
			}
		}

		dialectHistory[dialect.name] = diffRecord
	}

	stateSource, err := marshalDDLStateFile(version, previousState, currentSnapshot, recordTables, dialectHistory)
	if err != nil {
		return ddlArtifacts{}, errors.Trace(err)
	}

	models = append(models, ddlFileModel{
		Filename: filepath.Join(outDir, ddlStateFilename),
		Source:   stateSource,
	})

	sort.Slice(models, func(i, j int) bool {
		return models[i].Filename < models[j].Filename
	})

	return ddlArtifacts{
		models:       models,
		firstRun:     firstRun,
		hasChange:    hasChange,
		recordTables: append([]ddlStateRecordTable(nil), recordTables...),
	}, nil
}

func renderDDLHeader(version string) string {
	return fmt.Sprintf("%s%s. DO NOT EDIT.\n\n", ddlGeneratedFileHeaderPrefix, version)
}

func renderDDLTableBlock(
	table *tsq.StructInfo,
	dialect ddlDialectSpec,
	resolver *ddlTypeResolver,
) (string, error) {
	columnLines := make([]string, 0, len(table.Fields))
	for _, field := range orderedDDLFields(table) {
		desc, err := resolver.describeField(table, field)
		if err != nil {
			return "", errors.Annotatef(err, "failed to describe %s.%s", table.TypeInfo.TypeName, field.Name)
		}

		line, err := renderDDLColumnDefinition(table, field, desc, dialect)
		if err != nil {
			return "", errors.Annotatef(err, "failed to render column %s.%s", table.TypeInfo.TypeName, field.Name)
		}

		columnLines = append(columnLines, "    "+line)
	}

	var buf strings.Builder
	buf.WriteString(renderDDLCreateTable(table, columnLines, dialect))

	indexStatements, err := renderDDLIndexStatements(table, dialect)
	if err != nil {
		return "", errors.Trace(err)
	}

	for _, stmt := range indexStatements {
		buf.WriteString("\n\n")
		buf.WriteString(stmt)
	}

	return buf.String(), nil
}

func renderDDLCreateTable(table *tsq.StructInfo, columns []string, dialect ddlDialectSpec) string {
	var buf strings.Builder
	buf.WriteString("CREATE TABLE ")

	if clause := dialect.dialect.CreateTableIfNotExistsSuffix(); clause != "" {
		buf.WriteString(clause)
		buf.WriteByte(' ')
	}

	buf.WriteString(dialect.dialect.QuoteField(table.Table))
	buf.WriteString(" (\n")
	buf.WriteString(strings.Join(columns, ",\n"))
	buf.WriteString("\n)")
	buf.WriteString(dialect.dialect.CreateTableSuffix())

	return buf.String()
}

func renderDDLIndexStatements(table *tsq.StructInfo, dialect ddlDialectSpec) ([]string, error) {
	statements := make([]string, 0, len(table.UxList)+len(table.IdxList))

	for _, item := range []struct {
		unique bool
		items  []tsq.IndexInfo
	}{
		{unique: true, items: table.UxList},
		{unique: false, items: table.IdxList},
	} {
		for _, idx := range item.items {
			quotedFields := make([]string, 0, len(idx.Fields))
			for _, fieldName := range idx.Fields {
				field, ok := table.FieldMap[fieldName]
				if !ok {
					return nil, errors.Errorf("index %s references unknown field %s", idx.Name, fieldName)
				}

				quotedFields = append(quotedFields, dialect.dialect.QuoteField(field.Column))
			}

			quotedTable := dialect.dialect.QuoteField(table.Table)
			quotedIndex := dialect.dialect.QuoteField(idx.Name)

			switch dialect.name {
			case ddlDialectMySQL:
				uniqueClause := ""
				if item.unique {
					uniqueClause = "UNIQUE "
				}

				statements = append(statements, fmt.Sprintf(
					"ALTER TABLE %s ADD %sINDEX %s(%s)%s",
					quotedTable,
					uniqueClause,
					quotedIndex,
					strings.Join(quotedFields, ", "),
					dialect.dialect.CreateIndexSuffix(),
				))
			default:
				uniqueClause := ""
				if item.unique {
					uniqueClause = "UNIQUE "
				}

				statements = append(statements, fmt.Sprintf(
					"CREATE %sINDEX %s ON %s(%s)%s",
					uniqueClause,
					quotedIndex,
					quotedTable,
					strings.Join(quotedFields, ", "),
					dialect.dialect.CreateIndexSuffix(),
				))
			}
		}
	}

	return statements, nil
}

func orderedDDLFields(table *tsq.StructInfo) []tsq.FieldInfo {
	fields := make([]tsq.FieldInfo, 0, len(table.Fields))
	fields = append(fields, table.Fields...)

	rank := func(name string) int {
		switch name {
		case table.ID:
			return 0
		case table.CreatedAtField:
			return 1
		case table.UpdatedAtField:
			return 2
		case table.DeletedAtField:
			return 3
		case table.VersionField:
			return 4
		default:
			return 10
		}
	}

	sort.Slice(fields, func(i, j int) bool {
		leftRank := rank(fields[i].Name)

		rightRank := rank(fields[j].Name)
		if leftRank != rightRank {
			return leftRank < rightRank
		}

		if fields[i].Column != fields[j].Column {
			return fields[i].Column < fields[j].Column
		}

		return fields[i].Name < fields[j].Name
	})

	return fields
}

func renderDDLColumnDefinition(
	table *tsq.StructInfo,
	field tsq.FieldInfo,
	desc ddlColumnDescriptor,
	dialect ddlDialectSpec,
) (string, error) {
	quotedColumn := dialect.dialect.QuoteField(field.Column)
	isPrimaryKey := field.Name == table.ID

	if isPrimaryKey && table.AI {
		return renderDDLAutoIncrementPrimaryKeyColumn(quotedColumn, desc, dialect)
	}

	sqlType := renderDDLColumnType(desc, dialect)
	parts := []string{quotedColumn, sqlType}

	if isPrimaryKey {
		parts = append(parts, "PRIMARY KEY")
	} else if !desc.nullable {
		parts = append(parts, "NOT NULL")
	}

	if defaultValue := ddlManagedDefaultClause(table, field, desc); defaultValue != "" {
		parts = append(parts, "DEFAULT "+defaultValue)
	}

	return strings.Join(parts, " "), nil
}

func renderDDLAutoIncrementPrimaryKeyColumn(
	quotedColumn string,
	desc ddlColumnDescriptor,
	dialect ddlDialectSpec,
) (string, error) {
	if desc.kind != ddlColumnInt {
		return "", errors.New("auto-increment primary key requires an integer field")
	}

	switch dialect.name {
	case ddlDialectSQLite:
		return quotedColumn + " INTEGER PRIMARY KEY " + dialect.dialect.AutoIncrementClause(), nil
	case ddlDialectMySQL:
		return strings.Join([]string{
			quotedColumn,
			renderDDLColumnType(desc, dialect),
			"PRIMARY KEY",
			dialect.dialect.AutoIncrementClause(),
		}, " "), nil
	case ddlDialectPostgres:
		return quotedColumn + " " + renderDDLSerialType(desc), nil
	default:
		return "", errors.Errorf("unsupported dialect %s", dialect.name)
	}
}

func renderDDLSerialType(desc ddlColumnDescriptor) string {
	switch {
	case desc.bits <= 16:
		return "SMALLSERIAL PRIMARY KEY"
	case desc.bits <= 32:
		return "SERIAL PRIMARY KEY"
	default:
		return "BIGSERIAL PRIMARY KEY"
	}
}

func renderDDLColumnType(desc ddlColumnDescriptor, dialect ddlDialectSpec) string {
	switch dialect.name {
	case ddlDialectSQLite:
		return renderSQLiteColumnType(desc)
	case ddlDialectMySQL:
		return renderMySQLColumnType(desc)
	case ddlDialectPostgres:
		return renderPostgresColumnType(desc)
	default:
		return "TEXT"
	}
}

func renderSQLiteColumnType(desc ddlColumnDescriptor) string {
	switch desc.kind {
	case ddlColumnBool:
		return "BOOLEAN"
	case ddlColumnBytes:
		return "BLOB"
	case ddlColumnFloat:
		return "REAL"
	case ddlColumnInt:
		return "INTEGER"
	case ddlColumnString:
		return "TEXT"
	case ddlColumnTime:
		return "TIMESTAMP"
	default:
		return "TEXT"
	}
}

func renderMySQLColumnType(desc ddlColumnDescriptor) string {
	switch desc.kind {
	case ddlColumnBool:
		return "BOOLEAN"
	case ddlColumnBytes:
		return "BLOB"
	case ddlColumnFloat:
		if desc.bits <= 32 {
			return "FLOAT"
		}

		return "DOUBLE"
	case ddlColumnInt:
		base := "BIGINT"

		switch {
		case desc.bits <= 8:
			base = "TINYINT"
		case desc.bits <= 16:
			base = "SMALLINT"
		case desc.bits <= 32:
			base = "INT"
		}

		if desc.unsigned {
			base += " UNSIGNED"
		}

		return base
	case ddlColumnString:
		if desc.size > 0 {
			return fmt.Sprintf("VARCHAR(%d)", desc.size)
		}

		return "TEXT"
	case ddlColumnTime:
		return "DATETIME"
	default:
		return "TEXT"
	}
}

func renderPostgresColumnType(desc ddlColumnDescriptor) string {
	switch desc.kind {
	case ddlColumnBool:
		return "BOOLEAN"
	case ddlColumnBytes:
		return "BYTEA"
	case ddlColumnFloat:
		if desc.bits <= 32 {
			return "REAL"
		}

		return "DOUBLE PRECISION"
	case ddlColumnInt:
		switch {
		case desc.bits <= 16:
			return "SMALLINT"
		case desc.bits <= 32:
			return "INTEGER"
		default:
			return "BIGINT"
		}
	case ddlColumnString:
		if desc.size > 0 {
			return fmt.Sprintf("VARCHAR(%d)", desc.size)
		}

		return "TEXT"
	case ddlColumnTime:
		return "TIMESTAMP"
	default:
		return "TEXT"
	}
}

func ddlManagedDefaultClause(table *tsq.StructInfo, field tsq.FieldInfo, desc ddlColumnDescriptor) string {
	switch field.Name {
	case table.CreatedAtField:
		if desc.kind == ddlColumnTime && !desc.nullable {
			return "CURRENT_TIMESTAMP"
		}
	case table.DeletedAtField:
		if desc.kind == ddlColumnInt && !desc.nullable {
			return "0"
		}
	case table.VersionField:
		if desc.kind == ddlColumnInt && !desc.nullable {
			return "1"
		}
	}

	return ""
}

func newDDLTypeResolver(packagePath, dir string) (*ddlTypeResolver, error) {
	cfg, pattern, err := resolveDDLLoadRequest(packagePath, dir)
	if err != nil {
		return nil, err
	}

	pkgs, err := packages.Load(cfg, pattern)
	if err != nil {
		return nil, err
	}

	all := flattenLoadedPackages(pkgs)

	byPath := make(map[string]*packages.Package, len(all))
	for _, pkg := range all {
		if pkg.PkgPath == "" {
			continue
		}

		byPath[pkg.PkgPath] = pkg
	}

	if len(byPath) == 0 {
		return nil, errors.Errorf("failed to load package %s", packagePath)
	}

	return &ddlTypeResolver{packages: byPath}, nil
}

func resolveDDLLoadRequest(packagePath, dir string) (*packages.Config, string, error) {
	cfg := &packages.Config{
		Mode: packages.NeedName |
			packages.NeedFiles |
			packages.NeedImports |
			packages.NeedDeps |
			packages.NeedTypes |
			packages.NeedTypesInfo |
			packages.NeedSyntax |
			packages.NeedModule,
	}

	pattern := packagePath

	switch {
	case dir != "":
		absDir, err := filepath.Abs(dir)
		if err != nil {
			return nil, "", err
		}

		cfg.Dir = absDir
		pattern = "."
	case filepath.IsAbs(packagePath):
		cfg.Dir = packagePath
		pattern = "."
	case strings.HasPrefix(packagePath, "."):
		absPath, err := filepath.Abs(packagePath)
		if err != nil {
			return nil, "", err
		}

		cfg.Dir = absPath
		pattern = "."
	}

	if cfg.Dir != "" {
		overlay, err := buildDDLGeneratedFileOverlay(cfg.Dir)
		if err != nil {
			return nil, "", err
		}

		if len(overlay) > 0 {
			cfg.Overlay = overlay
		}
	}

	return cfg, pattern, nil
}

func buildDDLGeneratedFileOverlay(dir string) (map[string][]byte, error) {
	entries, err := os.ReadDir(dir)
	if os.IsNotExist(err) {
		return nil, nil
	}

	if err != nil {
		return nil, err
	}

	packageName, err := detectDDLPackageName(dir, entries)
	if err != nil {
		return nil, err
	}

	if packageName == "" {
		return nil, nil
	}

	overlay := make(map[string][]byte)

	for _, entry := range entries {
		if entry.IsDir() || !strings.HasSuffix(entry.Name(), "_tsq.go") {
			continue
		}

		overlay[filepath.Join(dir, entry.Name())] = []byte("package " + packageName + "\n")
	}

	return overlay, nil
}

func detectDDLPackageName(dir string, entries []os.DirEntry) (string, error) {
	fileSet := token.NewFileSet()

	for _, entry := range entries {
		if entry.IsDir() {
			continue
		}

		name := entry.Name()
		if !strings.HasSuffix(name, ".go") || strings.HasSuffix(name, "_test.go") || strings.HasSuffix(name, "_tsq.go") {
			continue
		}

		file, err := goparser.ParseFile(fileSet, filepath.Join(dir, name), nil, goparser.PackageClauseOnly)
		if err != nil {
			return "", err
		}

		return file.Name.Name, nil
	}

	return "", nil
}

func flattenLoadedPackages(roots []*packages.Package) []*packages.Package {
	seen := make(map[string]struct{})
	ordered := make([]*packages.Package, 0)

	var visit func(*packages.Package)
	visit = func(pkg *packages.Package) {
		if pkg == nil {
			return
		}

		key := pkg.PkgPath
		if key == "" {
			key = pkg.ID
		}

		if _, ok := seen[key]; ok {
			return
		}

		seen[key] = struct{}{}

		ordered = append(ordered, pkg)

		importPaths := make([]string, 0, len(pkg.Imports))
		for path := range pkg.Imports {
			importPaths = append(importPaths, path)
		}

		sort.Strings(importPaths)

		for _, path := range importPaths {
			visit(pkg.Imports[path])
		}
	}

	for _, root := range roots {
		visit(root)
	}

	return ordered
}

func (r *ddlTypeResolver) describeField(
	table *tsq.StructInfo,
	field tsq.FieldInfo,
) (ddlColumnDescriptor, error) {
	namedType, pkg, err := r.lookupNamedStruct(table.TypeInfo)
	if err != nil {
		return ddlColumnDescriptor{}, errors.Trace(err)
	}

	varObj, tag, err := lookupDDLField(namedType, pkg, field.Name)
	if err != nil {
		return ddlColumnDescriptor{}, errors.Trace(err)
	}

	return classifyDDLColumnType(varObj.Type(), tag)
}

func (r *ddlTypeResolver) lookupNamedStruct(typeInfo tsq.TypeInfo) (*types.Named, *types.Package, error) {
	pkg, ok := r.packages[typeInfo.Package.Path]
	if !ok || pkg.Types == nil {
		return nil, nil, errors.Errorf("loaded type package %s not found", typeInfo.Package.Path)
	}

	obj := pkg.Types.Scope().Lookup(typeInfo.TypeName)
	if obj == nil {
		return nil, nil, errors.Errorf("type %s not found in package %s", typeInfo.TypeName, typeInfo.Package.Path)
	}

	typeName, ok := obj.(*types.TypeName)
	if !ok {
		return nil, nil, errors.Errorf("%s in package %s is not a named type", typeInfo.TypeName, typeInfo.Package.Path)
	}

	named, ok := typeName.Type().(*types.Named)
	if !ok || underlyingStructType(named) == nil {
		return nil, nil, errors.Errorf("%s is not a struct type", typeInfo.TypeName)
	}

	return named, pkg.Types, nil
}

func lookupDDLField(named *types.Named, pkg *types.Package, fieldName string) (*types.Var, string, error) {
	obj, index, _ := types.LookupFieldOrMethod(named, false, pkg, fieldName)
	if obj == nil {
		obj, index, _ = types.LookupFieldOrMethod(types.NewPointer(named), false, pkg, fieldName)
		if obj == nil {
			return nil, "", errors.Errorf("field %s not found", fieldName)
		}
	}

	fieldObj, ok := obj.(*types.Var)
	if !ok || !fieldObj.IsField() {
		return nil, "", errors.Errorf("%s is not a field", fieldName)
	}

	tag, err := lookupDDLFieldTag(named, index)
	if err != nil {
		return nil, "", errors.Trace(err)
	}

	return fieldObj, tag, nil
}

func lookupDDLFieldTag(root types.Type, index []int) (string, error) {
	current := root
	tag := ""

	for _, item := range index {
		structType := underlyingStructType(current)
		if structType == nil {
			return "", errors.New("field index does not resolve to a struct")
		}

		tag = structType.Tag(item)
		current = structType.Field(item).Type()
	}

	return tag, nil
}

func underlyingStructType(t types.Type) *types.Struct {
	switch value := t.(type) {
	case *types.Named:
		return underlyingStructType(value.Underlying())
	case *types.Pointer:
		return underlyingStructType(value.Elem())
	case *types.Struct:
		return value
	default:
		return nil
	}
}

func classifyDDLColumnType(t types.Type, rawTag string) (ddlColumnDescriptor, error) {
	opts := parseDDLTagOptions(reflect.StructTag(rawTag).Get("db"))

	desc, err := classifyDDLColumnTypeRecursive(t, opts.size, false)
	if err != nil {
		return ddlColumnDescriptor{}, err
	}

	if desc.kind == ddlColumnString {
		desc.size = opts.size
	}

	return desc, nil
}

func classifyDDLColumnTypeRecursive(
	t types.Type,
	size int,
	nullable bool,
) (ddlColumnDescriptor, error) {
	switch value := t.(type) {
	case *types.Pointer:
		return classifyDDLColumnTypeRecursive(value.Elem(), size, true)
	case *types.Named:
		if pkg := value.Obj().Pkg(); pkg != nil {
			switch pkg.Path() {
			case importPathTime:
				if value.Obj().Name() == "Time" {
					return ddlColumnDescriptor{kind: ddlColumnTime, nullable: nullable}, nil
				}
			case importPathDatabaseSQL:
				switch value.Obj().Name() {
				case "NullBool":
					return ddlColumnDescriptor{kind: ddlColumnBool, nullable: true}, nil
				case "NullFloat64":
					return ddlColumnDescriptor{kind: ddlColumnFloat, bits: 64, nullable: true}, nil
				case "NullInt64":
					return ddlColumnDescriptor{kind: ddlColumnInt, bits: 64, nullable: true}, nil
				case "NullString":
					return ddlColumnDescriptor{kind: ddlColumnString, nullable: true, size: size}, nil
				case "NullTime":
					return ddlColumnDescriptor{kind: ddlColumnTime, nullable: true}, nil
				}
			default:
				if strings.HasPrefix(pkg.Path(), nullbioImportPrefix) {
					switch value.Obj().Name() {
					case "Time":
						return ddlColumnDescriptor{kind: ddlColumnTime, nullable: true}, nil
					case "String":
						return ddlColumnDescriptor{kind: ddlColumnString, nullable: true, size: size}, nil
					case "Bool":
						return ddlColumnDescriptor{kind: ddlColumnBool, nullable: true}, nil
					case "Float32":
						return ddlColumnDescriptor{kind: ddlColumnFloat, bits: 32, nullable: true}, nil
					case "Float64":
						return ddlColumnDescriptor{kind: ddlColumnFloat, bits: 64, nullable: true}, nil
					case "Int", "Int8", "Int16", "Int32", "Int64":
						return ddlColumnDescriptor{kind: ddlColumnInt, bits: 64, nullable: true}, nil
					case "Uint", "Uint8", "Uint16", "Uint32", "Uint64":
						return ddlColumnDescriptor{kind: ddlColumnInt, bits: 64, unsigned: true, nullable: true}, nil
					}
				}
			}
		}

		return classifyDDLColumnTypeRecursive(value.Underlying(), size, nullable)

	case *types.Basic:
		info := value.Info()
		switch {
		case info&types.IsBoolean != 0:
			return ddlColumnDescriptor{kind: ddlColumnBool, nullable: nullable}, nil
		case info&types.IsString != 0:
			return ddlColumnDescriptor{kind: ddlColumnString, nullable: nullable, size: size}, nil
		case info&types.IsFloat != 0:
			bits := 64
			if value.Kind() == types.Float32 {
				bits = 32
			}

			return ddlColumnDescriptor{kind: ddlColumnFloat, bits: bits, nullable: nullable}, nil
		case info&types.IsInteger != 0:
			return ddlBasicIntegerDescriptor(value, nullable), nil
		}
	case *types.Slice:
		if elem, ok := value.Elem().(*types.Basic); ok && elem.Kind() == types.Byte {
			return ddlColumnDescriptor{kind: ddlColumnBytes, nullable: nullable}, nil
		}
	}

	return ddlColumnDescriptor{}, errors.Errorf("unsupported DDL field type %s", types.TypeString(t, nil))
}

func ddlBasicIntegerDescriptor(basic *types.Basic, nullable bool) ddlColumnDescriptor {
	desc := ddlColumnDescriptor{kind: ddlColumnInt, bits: 64, nullable: nullable}

	switch basic.Kind() {
	case types.Int8:
		desc.bits = 8
	case types.Int16:
		desc.bits = 16
	case types.Int32:
		desc.bits = 32
	case types.Int64, types.Int:
		desc.bits = 64
	case types.Uint8:
		desc.bits = 8
		desc.unsigned = true
	case types.Uint16:
		desc.bits = 16
		desc.unsigned = true
	case types.Uint32:
		desc.bits = 32
		desc.unsigned = true
	case types.Uint64, types.Uint, types.Uintptr:
		desc.bits = 64
		desc.unsigned = true
	}

	return desc
}

type ddlTagOptions struct {
	size int
}

func parseDDLTagOptions(dbTag string) ddlTagOptions {
	opts := ddlTagOptions{}
	if dbTag == "" {
		return opts
	}

	parts := strings.Split(dbTag, ",")
	for _, part := range parts[1:] {
		key, value, ok := strings.Cut(part, ":")
		if !ok {
			continue
		}

		if key != "size" {
			continue
		}

		n, err := strconv.Atoi(value)
		if err != nil || n <= 0 {
			continue
		}

		opts.size = n
	}

	return opts
}

func buildDDLPlan(models []ddlFileModel, dir string) ([]generationPlanEntry, error) {
	plan := make([]generationPlanEntry, 0, len(models))
	plannedFiles := make(map[string]struct{}, len(models))

	for _, model := range models {
		status, err := ddlPlanStatusFor(model.Filename, model.Source)
		if err != nil {
			return nil, errors.Annotatef(err, "failed to plan DDL file: %s", model.Filename)
		}

		plan = append(plan, generationPlanEntry{
			Filename: model.Filename,
			Source:   model.Source,
			Status:   status,
		})
		plannedFiles[model.Filename] = struct{}{}
	}

	staleFiles, err := findStaleDDLFiles(dir, plannedFiles)
	if err != nil {
		return nil, errors.Trace(err)
	}

	for _, stale := range staleFiles {
		plan = append(plan, generationPlanEntry{
			Filename: stale,
			Status:   generationPlanStale,
		})
	}

	sort.Slice(plan, func(i, j int) bool {
		return plan[i].Filename < plan[j].Filename
	})

	return plan, nil
}

func ddlPlanStatusFor(filename string, src []byte) (generationPlanStatus, error) {
	existing, err := os.ReadFile(filename)
	if os.IsNotExist(err) {
		return generationPlanCreate, nil
	}

	if err != nil {
		return "", err
	}

	if bytes.Equal(existing, src) {
		return generationPlanUnchanged, nil
	}

	if err := ensureWritableDDLFile(filename); err != nil {
		return "", err
	}

	return generationPlanUpdate, nil
}

func findStaleDDLFiles(dir string, plannedFiles map[string]struct{}) ([]string, error) {
	entries, err := os.ReadDir(dir)
	if os.IsNotExist(err) {
		return nil, nil
	}

	if err != nil {
		return nil, err
	}

	stale := make([]string, 0)

	for _, entry := range entries {
		if entry.IsDir() {
			continue
		}

		name := entry.Name()
		if !strings.HasSuffix(name, ".sql") && name != ddlStateFilename {
			continue
		}

		filename := filepath.Join(dir, name)
		if _, ok := plannedFiles[filename]; ok {
			continue
		}

		content, err := os.ReadFile(filename)
		if err != nil {
			return nil, err
		}

		if !isGeneratedDDLArtifact(content) {
			continue
		}

		stale = append(stale, filename)
	}

	sort.Strings(stale)

	return stale, nil
}

func writeDDLFile(filename string, src []byte) error {
	if err := ensureWritableDDLFile(filename); err != nil {
		return err
	}

	if err := os.MkdirAll(filepath.Dir(filename), 0o755); err != nil {
		return err
	}

	perm := os.FileMode(0o644)
	if info, err := os.Stat(filename); err == nil {
		perm = info.Mode().Perm()
	} else if !os.IsNotExist(err) {
		return err
	}

	dir := filepath.Dir(filename)
	pattern := "." + filepath.Base(filename) + ".tmp-*"

	tmpFile, err := os.CreateTemp(dir, pattern)
	if err != nil {
		return err
	}

	tmpName := tmpFile.Name()

	defer func() {
		_ = os.Remove(tmpName)
	}()

	if err := tmpFile.Chmod(perm); err != nil {
		_ = tmpFile.Close()
		return err
	}

	if _, err := tmpFile.Write(src); err != nil {
		_ = tmpFile.Close()
		return err
	}

	if err := tmpFile.Close(); err != nil {
		return err
	}

	return os.Rename(tmpName, filename)
}

func ensureWritableDDLFile(filename string) error {
	existing, err := os.ReadFile(filename)
	if os.IsNotExist(err) {
		return nil
	}

	if err != nil {
		return err
	}

	if len(existing) == 0 || isGeneratedDDLArtifact(existing) {
		return nil
	}

	return errors.Errorf("refusing to overwrite non-generated file: %s", filename)
}

func isGeneratedDDLArtifact(content []byte) bool {
	if bytes.HasPrefix(content, []byte(ddlGeneratedFileHeaderPrefix)) {
		return true
	}

	var meta struct {
		GeneratedBy string `json:"generated_by"`
	}

	if err := json.Unmarshal(content, &meta); err != nil {
		return false
	}

	return strings.HasPrefix(meta.GeneratedBy, "tsq-")
}
